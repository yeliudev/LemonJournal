const { mysql } = require('../qcloud')

module.exports = async ctx => {
    var open_id_object = await mysql('cSessionInfo').where({ skey: ctx.header.skey }).select('open_id').first()
    if (open_id_object) {
        // 数据库存在 skey ，验证通过
        try {
            await mysql('journal_book').update({ name: ctx.request.body.name }).where({ journal_book_id: ctx.request.body.journal_book_id })
            await mysql('journal_book').update({ background_id: ctx.request.body.background_id }).where({ journal_book_id: ctx.request.body.journal_book_id })

            ctx.body = {
                success: true
            }
        } catch (error) {
            ctx.body = {
                success: false,
                errMsg: error
            }
        }
    } else {
        // 查询结果为 undefined ，验证不通过
        ctx.body = {
            success: false,
            errMsg: 'skey不存在，验证不通过'
        }
    }
}
